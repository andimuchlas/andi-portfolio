    async start() {
        const container = document.getElementById('terminal-content');
        document.body.classList.add('power-on');

        // Initial darkness
        container.innerHTML = '';
        container.style.opacity = '0';

        // Create Logo Container
        const logoDiv = document.createElement('div');
        logoDiv.style.cssText = `
            font-size: 0.6vmin;
            line-height: 1;
            text-align: center;
            width: 100%;
            margin-top: 5vmin;
            margin-bottom: 5vmin;
            color: #FFB000;
            opacity: 0;
            transition: opacity 2s ease-in;
        `;
        logoDiv.innerHTML = this.asciiBootLogo;
        container.appendChild(logoDiv);

        // Start Sound and Fade In Logo
        window.Audio.playBoot();

        // First, fade the whole container slightly
        container.style.transition = 'opacity 1s ease-in';
        container.style.opacity = '1';

        // Then fade the logo specifically
        await new Promise(r => setTimeout(r, 500));
        logoDiv.style.opacity = '1';

        await new Promise(r => setTimeout(r, 2500));

        // Disable keyboard capture during boot
        document.getElementById('keyboard-capture').disabled = true;

        for (let i = 0; i < this.bootText.length; i++) {
            const line = this.bootText[i];
            const div = document.createElement('div');
            container.appendChild(div);

            // Type the line character by character
            for (let j = 0; j < line.length; j++) {
                const char = line[j];
                div.innerHTML += char === ' ' ? '&nbsp;' : char;

                if (char !== ' ' && char !== '\n' && j % 2 === 0) {
                    window.Audio.playKeystroke();
                }

                const delay = line.includes('в”Ѓ') ? 1 : 8 + Math.random() * 12;
                await new Promise(r => setTimeout(r, delay));
            }

            if (line.trim() !== "" && !line.includes('в”Ѓ')) {
                // Add blinking cursor while "processing" line
                const cursor = document.createElement('span');
                cursor.className = 'retro-blink';
                cursor.innerHTML = '_';
                div.appendChild(cursor);

                window.Audio.playBootBeep();
                await new Promise(r => setTimeout(r, 400 + Math.random() * 600));

                cursor.remove();
            } else {
                await new Promise(r => setTimeout(r, 40));
            }
        }

        // Draw progress bar
        const pbContainer = document.createElement('div');
        pbContainer.className = 'progress-bar-container';
        pbContainer.style.marginTop = '1vmin';
        container.appendChild(pbContainer);

        let progressStr = "[";
        for (let i = 0; i < 24; i++) {
            progressStr += " ";
        }
        progressStr += "] 0%";
        pbContainer.innerHTML = progressStr.replace(/ /g, '&nbsp;');

        for (let i = 1; i <= 24; i++) {
            await new Promise(r => setTimeout(r, 50));
            let str = "[";
            for (let j = 0; j < 24; j++) {
                if (j < i) str += "в–€";
                else str += "&nbsp;";
            }
            const pct = Math.floor((i / 24) * 100);
            str += `] ${pct}%`;
            pbContainer.innerHTML = str;

            if (i % 2 === 0) window.Audio.playBootBeep();
        }

        await new Promise(r => setTimeout(r, 500));
        window.Renderer.appendHTML(`<br><div class="phosphor-fade-in retro-blink" style="cursor:pointer;">System ready. Press any key or click to continue...</div>`);

        // Wait for any key or click
        document.getElementById('keyboard-capture').disabled = false;
        document.getElementById('keyboard-capture').focus();

        const proceed = (e) => {
            if (e) e.stopImmediatePropagation();
            document.removeEventListener('keydown', proceed);
            document.removeEventListener('click', proceed);
            window.Audio.playEnter();
            window.MenuManager.showMenu();
        };

        document.addEventListener('keydown', proceed);
        document.addEventListener('click', proceed);
    }
